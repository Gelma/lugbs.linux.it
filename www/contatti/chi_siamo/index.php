<H3 align=center>Chi Siamo</H3>
<p>
Il Linux User Group di Brescia &egrave; un punto di incontro per le persone accomunate
dalla passione e per l'informatica, e per le tematiche care alla
<a HREF="http://www.gnu.org">Free Software Foundation</a>, alle quali in genere
ci si riferisce parlando di licenze <a HREF="http://www.gnu.org/philosophy/free-sw.it.html">
GPL</a> e <a HREF="http://www.opensource.org/docs/definition.html">Open Source</a>
(per quanto queste siano simili ma non 
<a HREF="http://www.gnu.org/philosophy/free-software-for-freedom.it.html">identiche</a>).
<p>
&Egrave; bene sottolineare come il LugBS, spontaneamente formatosi nel giugno del '98,
non esista in termini legali e che, quindi, quanto
venga realizzato, in termini di incontri, seminari et similia, sia frutto semplicemente
del tempo e dei mezzi donati dagli associati (dove per associato si intende 
chiunque dia una mano...).
<p>
Per essere coinvolti nella vita del LugBS non &egrave; richiesta nessuna capacit&agrave;, o
requisito, particolare. Al momento si contano soci di ogni et&agrave;, da giovani studenti
ad attempati professori universitari,
con le mansioni pi&ugrave; svariate, mossi dalle ragioni pi&ugrave; disparate (semplice curiosit&agrave;
verso un ramo del mondo informatico cos&igrave; in fermento, oppure interesse, per ragioni
lavorative e non, di imparare e ricevere aiuto, ecc).
<p>
&Egrave; bene ribadire che non esistono forme di iscrizione o affiliazione
ufficiali, e che se si vuole partecipare, o anche semplicemente
incontrare e conoscere gli altri membri del LugBS, &egrave; sufficiente
essere presenti alle varie iniziative o, volendo, proporne di nuove.
<p>
Il punto di discussione e incontro principale del LugBS &egrave;, sostanzialmente, la
<a href="http://ml.lugbs.linux.it">mailing list</a>.
Da l&igrave; si possono ottenere aiuti, si pu&ograve; partecipare alla realizzazione
dei vari eventi, proporre iniziative, essere aggiornati sulle ultime novit&agrave;.
<p>Per qualsiasi eventuale interrogativo, o esigenza, si consiglia di mandare direttamente un
messaggio alla <a href="http://ml.lugbs.linux.it">mailing list</a> principale, oppure agli
<a href="mailto:vips@lugbs.linux.it">amministratori</a>, fermo restando la possibilit&agrave; di contattare
direttamente i responsabili dei vari progetti
consultando l'<a href="attivisti.php">elenco</a> disponibile.
