<H3 align=center>Quinto Linux Day -  Linux: usarlo e capirlo</H3>
A tutti i <i>Linuxisti</i> della provincia di Brescia (e non):
<p>
Prosegue la fortunata serie di incontri sul sistema operativo
Linux organizzata dal Linux User Group di Brescia (lugBS) in
collaborazione con il Dipartimento di Matematica e Fisica della
Università Cattolica di Brescia.
<p>
L'evento si svolgerà Sabato 31 Marzo 2001 dalle 9:00 alle 12:30
nell'aula magna della sede del "Buon Pastore", presso
l'Università Cattolica di Brescia, via Musei 41;
considerato l'affollamento dei precedenti incontri e il notevole
interesse degli argomenti trattati, è consigliabile iscriversi
mandando una mail a <a href="mailto:lug-eventi@lugbs.linux.it">
lug-eventi@lugbs.linux.it</a> specificando nome e cognome di ogni
partecipante (l'iscrizione è comunque gratuita).
<p>
Per informazioni logistiche: <br>
        Segreteria del Dipartimento di Matematica e Fisica <br>
        Telefono: 030-2406702 <br>
<p>
Per informazioni sul LUG della provincia di Brescia
(in particolare per partecipare alle attività) è possibile consultare
il sito del lugBS: <a href="http://lugbs.linux.it">http://lugbs.linux.it</a>
<p>
Saluti a tutti.
