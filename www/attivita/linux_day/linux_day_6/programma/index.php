<p>Target: rivolto principalmente agli insegnanti delle scuole secondarie,
nonché delle scuole di ordine inferiore, agli studenti e al personale
tecnico delle stesse.
<p>L'incontro è motivato dalla crescente spinta verso l'informatizzazione
delle scuole, che si viene spesso a scontrare con i costi e i vincoli
imposti dalle più diffuse modalità di licenza dei programmi più
diffusi.  Il software libero, e Linux in particolare, può costituire un
vantaggioso complemento o una valida alternativa all'impiego di
soluzioni proprietarie. Il fine di questo incontro è sensibilizzare e
presentare alle scuole questa possibilità.
