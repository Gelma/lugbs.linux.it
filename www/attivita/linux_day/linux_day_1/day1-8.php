<h3 align="center">11/06/99 - Come utilizzare Linux per uso personale</b>
<hr align="LEFT" size="1" noshade>
<p><font color="red"><b><i>Definizione di un percorso di crescita.</i></b></font>
Questo intervento ha lo scopo di
fornire una traccia all'utente novizio che volgia avvicinarsi a
Linux.
<p><i>La scelta della distribuzione</i><br>
 Le principali distribuzioni hanno un sistema di installazione
guidato sufficientemente semplice ed intuitivo.<br>
 A prescindere dalla distribuzione, forse i due passi pi&ugrave;
"difficili" per un neofita rimangono il partizionamento del disco e
l'installazione del Loader del sistema operativo.<br>
 In realt&agrave; alcune distribuzioni offrono la
possibilit&agrave; di installare Linux direttamente su una
partizione Windows o DOS, oppure offrono la possibilit&agrave; di
utilizzare una versione "live" contenuta sul CD-ROM; purtroppo, per
un utilizzo serio, entrambe le soluzioni sono limitative e
inefficienti.<br>
 Per quanto riguarda la disponibilit&agrave; del software, tutte le
distribuzioni offrono numerosi programmi, oltre ovviamente alla
possibilit&agrave; di "scaricarne" di nuovi; in particolare in
questo caso le distribuzioni si possono "differenziare" in base al
tipo di pacchetti: RPM (RedHat, Caldera, SuSE, Mandrake), DEB
(Debian) o TGZ (Slackware).<br>
 Sul fronte dell'aderenza allo "spirito" OpenSource, va detto che
solo la Debian pu&ograve; "vantarsi" di avere contribuito
attivamente alla definizione della licenza GPL e di essere
pienamente conforme a tale licenza.<br>
<p><i>La documentazione</i><br>
 Ad una prima analisi, pu&ograve; sembrare che Linux sia carente di
documentazione (anche se oramai esistono numerosi libri
sull'argomento), in realt&agrave; (e qui si pu&ograve; riscontrare
l'eredit&agrave; Unix) &egrave; ricchissimo di documentazione:
<ul>
<li><i>manuali d'installazione e d'uso</i></li>
<li><i>pagine di manuale (coprono i comandi utente, i comandi dell'amministratore,
le funzioni di sistema, le funzioni C, la sinstassi dei file di configurazione, ...)</i></li>
<li><i>howto (i "bigini" per risolvere problemi specifici)</i></li>
<li><i>faq (le domande pi&ugrave; frequenti con la relativa risposta)</i></li>
<li><i>documenti del progetto LDP (Linux Documentation Project) con alcune traduzioni</i></li>
<li><i>appunti di Linux (eccezionale opera "nostrana")</i></li>
<li><i>guide e help in linea di programmi KDE e GNOME</i></li>
</ul>
