<p><font color="red"><b>Il futuro di Linux (Andrea Mauro)</b></font>
<p>Questo intervento conclusivo cercher&agrave; di delineare quali
potrebbero essere le linee evolutive del mondo GNU/Linux.
<p><i>Lato client/workstation</i><br>
 Su questo fronte un grosso contribuito &egrave; stato dato dai
"desktop manager" (KDE/GNOME) che forniscono interfacce e funzioni
coerenti, sia all'utente che al programmatore.<br>
 Il grosso passo sar&agrave; la realizzazione di applicazioni
completamente OpenSource (in particolare di una suite Office-like)
che nel giro di prossimi anni (o mesi) potrebbero rendere la
piattaforma Linux in una valida soluzione personale e/o ufficio.
Per ora gnumeric/abiword, siag e koffice lasciano intravedere quali
potranno essere i "prodotti" Linux per questo settore.<br>
 Per applicazioni "verticali" (come il CAD) &egrave; probabile che
le soluzioni OpenSource non arriveranno (o che non arriveranno in
tempi brevi).
<p><i>Lato server</i><br>
 Su questo fronte le potenzialit&agrave; di Linux e delle soluzioni
OpenSource sono dimostrate dalla diffusione delle stesse.<br>
 Sono oramai diventati famosi i sistemi cluster e i sistemi di
calcolo parallelo utilizzati dalla NASA, basati su piattaforme Linux.<br>
 Alcune aree, tipo i sistemi ERP, che oggi sono (parzialmente)
scoperte, saranno presto colmate da prodotti commerciali. Per
esempio SAP e Lotus hanno dichiarato di "portare" i loro prodotti
anche su architettura Linux.
<p><i>Lato embedded</i><br>
 Questo &egrave; sicuramente il fronte pi&ugrave; interessante,
perch&eacute; potrebbe coprire tutti i dispositivi elettronici
(computer esclusi) e va dai GSM, alle console per videogame, alle
autoradio, ai sistemi industriali... al frigorifero di casa.<br>
 Qualche spunto arriva dalla prima autoradio commerciale per
riprodurre brani in MP3
(<a href="http://www.empeg.org">www.empeg.org</a>), dalle voci (pi&ugrave; o
meno ufficiali) sulla PlayStation 2, dal server pi&ugrave; piccolo
del mondo (grosso come una carta di credito), ai sistemi di
navigazione "intelligenti".<br>
 Molto si giocher&agrave; nel campo dei telefonini e degli
elettrodomestici intelligenti dove i concorrenti pi&ugrave;
agguerriti sono WinCE e Jini.
<p><i>Ma qual &egrave; l'obiettivo ultimo?</i><br>
 A chi criticava che il progetto Linux non ha "piani a lungo
termine", Torvald (ironicamente) ha risposto che il suo obiettivo
&egrave; la dominazione del mondo.<br>
 Stando alla "legge di Torvalds" (nome puramente inventato) la
crescita di Linux &egrave; tale che tra 4 anni questo obiettivo
potrebbe essere raggiunto, nel senso che ci saranno pi&ugrave;
sistemi Linux di quanti sono gli abitanti sulla Terra.<br>
 D'altra parte &egrave; ovvio che il rapporto macchina/uomo non
sar&agrave; (e non potr&agrave; mai essere) 1:1; se si considerano
i server, i PC personali, i sistemi embedded &egrave; evidente che
ad ogni persona potrebbe "corrispondere" un certo numero di
sistemi.
<p><i>Ma quali saranno gli utenti di Linux?</i><br>
 Su questo argomento si &egrave; discusso a molto ed &egrave;
evidente, che per ora, un utente Linux &egrave; una persona
caratterizzata da un certo interesse nel campo informatico
(tipicamente chi vuole imparare qualcosa di computer, di
programmazione, di reti, si rivolge al Linux.<br>
 D'altra parte, in questi ultimi anni le distribuzioni Linux si
sono "semplificate" sensibilmente nella fase di installazione e
forniscono strumenti grafici sempre pi&ugrave; potenti per l'uso
anche di un utente "un po' pi&ugrave; svogliato".<br>
 Finora una delle frasi ricorrenti &egrave; stata "Linux non
&egrave; per tutti", ma &egrave; probabile che questo alibi sia
destinato a cadere nei prossimi anni (o nei prossimi mesi).
