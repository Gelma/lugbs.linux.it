<h3 align="center">11/06/99 - Come utilizzare Linux per uso personale</h3>
<hr align="LEFT" size="1" noshade>
<p><font color="red"><b><i>Linux per i programmatori (Francesco Tomasoni)</i></b></font>
Nella prima parte verranno analizzati i "tipi" di linguaggi
supportati da Linux (script/interpretati e compilati).<br>
 Per i linguaggi di script si considereranno i liguaggi pi&ugrave;
utilizzati, il tipo di impiego (script di shell per l'uso
quotidiano, perl per i cgi, tcl/tk per le gui...), i vantaggi
(velocit&agrave; nel realizzare il programma, semplicit&agrave;,
...) e gli svantaggi ("lentezza" di esecuzione, alcune limitazioni
rispetto a C e C++).<br>
 Per i linguaggi compilati si faranno dei rapidi cenni a C, al
sistema di gestione dei progetti di Unix (make).<br>
 Saranno inoltre fatte alcune considerazioni sul il problema della
portabilit&agrave;, in particolare la portabilit&agrave; dei
linguaggi interpretati, la portabilit&agrave; dei sorgenti C, la
portabilit&agrave; dei formati binari (cenno agli emulatori binari
di Linux).
<p>La seconda parte servir&agrave;
invece per approffondire il linguaggio Java.<br>
 Seguir&agrave; (tempo permettendo) un esempio di compilazione con
logo LUGbs sulla macchina Linux disponibile (JDK 1.2 &egrave;
gi&agrave; disponibile nelle contrib di redhat) da vedere su
Netscape.<br>
<p><i>JAVA il linguaggio per l'internetworking</i><br>
 La caratteristica proprietaria del linguaggio di prescindere
dall'architettura gli consente di realizzare applicazioni client
che possano essere eseguite non solo come Applet su browser ma
anche come task "standalone" su qualsiasi macchina.<br>
<p><i>Divertirsi sviluppando in JAVA</i><br>
 Il paradigma principale &egrave; costituito dallo sviluppo
(coerente ANSI) ad oggetti orientati ma con le semplificazioni del
caso, come l'assenza di puntatori e degli obblighi di gestione di
stak e RAM etc. Inoltre sono ormai disponibili una grande
variet&agrave; di librerie grafiche, per il net., calcolo, sound,
...<br>
<p><i>Sviluppo JAVA su macchine LINUX</i><br>
 Lo sviluppo &egrave; reso semplice ed efficace dall'ambiente shell
e fs di tipo UNIX (ideale anche per lo svilupppo distribuito) con
l'aggiunta di possibilit&agrave; multi-compile (anche in versione
"just in time"). Sono disponibili ambienti RAD (come LCaf&egrave;,
VJDK, ..).<br>
<p><i>Costi nulli e massima stabilit&agrave;</i><br>
 La SUN (madre di JAVA) ha rilasciato, dopo 6 anni, la versione 1.2
(chi va piano....) meglio chiamata versione2. Il linguaggio E'
MATURO e stabile. Tali versioni del JDK (JAVA development kit) sono
prontamente rese disponibili per Linux (vedi di seguito). In
sostanza &egrave; possibile realizzare codice "FREE" ottimo su un
sistema operativo come Linux da distribuire in rete non soltanto
tramite FTP ma in RUN-TIME per qualunque SO!<br>
<p><i>Stato dell'arte</i><br>
 JDK per Linux viene distribuito (open-source, fatto salve le lib.
native) in Rete a <a href="http://www.blackdown.org">www.blackdown.org</a>.<br>
