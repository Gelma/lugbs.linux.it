<h3 align="center">Linux nelle scuole</h3>
<p><font color="red">Di cosa si tratta?</font>
<p>Grazie alle donazioni di hardware, accorse via via nel tempo e
<a href="mailto:didattica@lugbs.linux.it">sempre ben accette</a>,
e all'impegno di
<a href="mailto:emanuele.bonardi@lugbs.linux.it">Emanuele Bonardi</a>,
&egrave; stato possibile recuperare/ripristinare singole postazioni e server
da regalare alle scuole.
<p>Il punto fondamentale, per&ograve;, non &egrave; tanto, o solo,
permettere ad un istituto di accedere a del ferro gratuito, quanto
l'utilizzo di detto ferrame come pretesto per permettere agli
insegnanti interessati l'avvicinamento al mondo del
<a href="http://www.gnu.org/philosophy/philosophy.it.html">Software Libero</a>.

<p><font color="red">Come si partecipa?</font>
<p>Gli incontri sono liberi, gratuiti e aperti a tutti, anche se
il taglio degli argomenti &egrave; prettamente rivolto alle esigenze
didattiche.
<br>Per entrare in contatto con il gruppo si pu&ograve; scrivere a
<a href="mailto:didattica@lugbs.linux.it">didattica@lugbs.linux.it</a>.



<p>Gli incontri della stagione 2011 si divideranno tra Brescia e la 
zona di Darfo Boario Terme, vista la disponibilit&agrave; dell'<a href="http://www.istitutosantamariadinazareth.it/">Istituto 
Santa Maria di Nazareth</a> a Brescia, che si aggiunge alla ospitalit&agrave; dell'<a href="http://www.icdarfo2.it/">Istituto comprensivo "Darfo 2"</a>.
