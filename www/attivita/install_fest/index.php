<H3 align="center">Install Fest</H3>
<p>Un install fest &egrave; un momento di incontro dove, chi lo desidera,
pu&ograve; ricevere un aiuto nell'installazione di linux, sulla propria
macchina, da parte di utenti pi&ugrave; scafati. Vista anche la
crescente qualit&agrave;, e facilit&agrave; d'uso, degli installer
presenti nelle recenti distribuzioni, il Lug non si &egrave; mai
particolarmente adoperato in iniziative di questo tipo. Ci&ograve; non
significa che, a fronte di un numero ragionevole di richieste in mailing
list, il tutto non possa essere ripetuto.
