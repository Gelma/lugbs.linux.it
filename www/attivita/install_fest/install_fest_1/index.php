<H3 align="center">Install Fest</H3>
<p>
Il Linux User Group di Brescia sta organizzando un'install fest che si terr&agrave;
durante il mese di aprile 2002.
</p>
<p>
Per chi non sapesse cosa &egrave; un install fest, &egrave; presto detto: si tratta di un
incontro (molto informale) dove i partecipanti possono installare sui propri
personal computer il nostro amato sistema operativo, guidati dai volontari
del lug.
</p>
<p>
Nota: Nell'aula saranno a disposizione i monitor (quindi vi evitate il peso e
l'ingombro maggiore).
</p>
<p>
Questi sono i  prerequisiti che i partecipanti devono avere:

<ol>
<li>Computer almeno di classe pentium, con 32 mb di ram, scheda video VGA,
   lettore CD-ROM. Per quanto riguarda i portatili... ci proviamo! :)
<li>Partizione libera da almeno 1 GB e backup di tutti i dati importanti (non
   possiamo rispondere di eventuali perdite di dati).
<li>Da 2/3 cd vuoti  (a seconda della distribuzione scelta) per portarsi a
   casa il sistema operativo
<li>Minima conoscenza dei sistemi operativi (in soldoni: saper almeno
   installare Windows)
<li>Lettura del documento Da DOS a Linux
</ol>

<p>
Questo &egrave;  il modulo da compilare ed inviare a <a href="mailto:morriz@lugbs.linux.it">morriz@lugbs.linux.it</a> per
l'ammissione. L'indirizzo rimane a disposizione anche per eventuali
chiarimenti.
</p>
<p>
(In questa fase, il modulo ha solo lo scopo di poter capire quale &egrave;
l'interesse per un install-fest. Verr&agrave;  chiesta una conferma nel momento in
cui verr&agrave; decisa la data precisa)
</p>
