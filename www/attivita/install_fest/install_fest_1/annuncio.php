<p>Per chi non sapesse cosa &egrave; un install fest, &egrave;
presto detto: si tratta di un<br>
incontro (molto informale) dove i partecipanti possono installare
sui propri<br>
personal computer il nostro amato sistema operativo, guidati dai
volontari<br>
del lug.<br>
<p>Nota: Nell'aula saranno a disposizione i monitor (quindi vi
evitate il peso e<br>
l'ingombro maggiore).<br>
<p>Prerequisiti:<br>
<p>1) Computer almeno di classe pentium, con 32 mb di ram, scheda
video VGA,<br>
&nbsp; &nbsp;lettore CD-ROM. Per quanto riguarda i portatili... ci
proviamo! :)<br>
2) Partizione libera da almeno 1 GB e backup di tutti i dati
importanti (non<br>
&nbsp; &nbsp;possiamo rispondere di eventuali perdite di dati).<br>
3) Da 2/3 cd vuoti &nbsp;(a seconda della distribuzione scelta) per
portarsi a<br>
&nbsp; &nbsp;casa il sistema operativo<br>
4) Minima conoscenza dei sistemi operativi (in soldoni: saper
almeno<br>
&nbsp; &nbsp;installare Windows)<br>
5) Lettura del documento "Da DOS a Linux" reperibile
all'indirizzo<br>
<p><a href=
"http://www.ibiblio.org/pub/Linux/docs/HOWTO/translations/italian/DOS-to-Linux-HOWTO">
http://www.ibiblio.org/pub/Linux/docs/HOWTO/translations/italian/DOS-to-Linux-HOWTO</a><br>
