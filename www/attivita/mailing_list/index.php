<H3 align="center">Mailing List</H3>
<p>La vita del LugBS ruota principalmente attorno alle mailing
list. &Egrave; qui dove si prendono le decisioni, si
discute delle iniziative e dei progetti futuri.
<p>Le liste attualmente attive sono:
<ul>
   <li><b>lug@lugbs.linux.it</b>: l'equivalente della piazza di paese. Vi si
   pu&ograve; discutere di qualsiasi tematica legata al Lug.
   <li><b>vips@lugbs.linux.it</b>: per chi si occupa dei progetti
   del lugBS. A differenza delle altre non &egrave; pubblica, 
   ma questo non significa che chiunque non ne
   possa fare parte.
</ul>

<H3><a name="iscrizione"></a>Utilizzo della mailing list</H3>
<p>Tutte le operazioni relative alla mailing list
(subscribe/unsubscribe/accesso agli archivi) possono essere effettuate
dall'apposita <a href="/cgi-bin/mailman/listinfo/lug">interfaccia web</a>.

