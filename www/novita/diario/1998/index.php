<h5>Il brodo primordiale</h5>
<dl>
 <dt>
 Tarda primavera '98
 </dt>
 <dd>
Il '98 rimarrà nella storia dell'informatica come l'anno dell'enorme crescita di Linux nel mondo. Nella sperduta provincia di Brescia com'è la situazione?<br>
Succede molte persone stanno già utilizzando Linux, ciascuna con una motivazione ben precisa.
C'è chi è costretto a studiarlo all'Università, magari per finire la tesi, ma che ne verrà illuminato (o folgorato), fino a diventare un "profeta" del verbo Linux.<br>
C'è chi lo usa per scelta e chi invece è costretto (per motivi di lavoro o per altre necessità) ad utilizzare controvoglia altri sistemi operativi (è inutile dire quali...).<br>
C'è chi ne ha sentito parlare e vorrebbe saperne di più.
 <br><br>
 <i>Nota e riflessione serie: perché non è nato un lugBS prima del '98?</i><br>
Fondamentalmente fino a questa data la comunità Linux è stata relegata in nicchie ben definite (università, centri di ricerche, aziende innovative); la nascita di un LUG a Brescia avrebbe relegato il LUG stesso a uno di questi poli, magari con benefici immediati, ma senza il vero spirito che dovrebbe motivare i LUG (che è la crescita di Linux, ma soprattutto della comunità che lo compone).<br>
Solo con l'aggiunta dell'utente medio, dello smanettone che fino a ieri ha usato Windows, del cusioso è stato possibile realizzare un LUG che, prima di tutto, è un gruppo di amici (molti dei quali prima non si conoscevano assolutamente), di idee anche diverse, ma con la comune passione dell'informatica, ed in particolare di Linux.
 </dd>
</dl>
